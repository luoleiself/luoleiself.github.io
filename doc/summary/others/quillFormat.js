function t1(content) {
  if(!Array.isArray(content)) {
    return '';
  }
  const defaultQuillStyle = {size: '12px',font: 'SimSun',color: '#000000',background: null,underline: false,strike: false,italic: false,bold: false,align: 'left'};
  for(let i = 0;i < content.length;) {
    let item = content[i];
    let attrs = Object.assign({...defaultQuillStyle}, Object.hasOwn(item, 'attributes') ? item.attributes : {});
    item.insert = item.insert.trimStart();
    if (item.insert.length !== 0 && !/^\n+$/.test(item.insert)) {
        // 拆分所有的包含任意值的值
        let matched = item.insert.match(/[^\n]*(\n*)/ig).filter(Boolean);
        for (let j = 0;j < matched.length;) {
            if(/^\n+$/.test(matched[j])){
                matched.splice(j, 1);
                continue;
            }
            j++;
        }
        content.splice(i, 1, ...matched.map(v => ({ attributes: attrs, insert: v })));
        i++;
        continue;
    }
    // 将 \n 的部分属性增补到前面所有非 \n 结尾的属性中
    let k = i - 1;
    while(k > -1) {
      const align = item?.attributes?.align ?? 'left';
      if(content[k].insert.endsWith('\n')) {
        break;
      }
      content[k].attributes = Object.assign({}, {...content[k].attributes}, { align: align });
      k--;
    }
    if(i - 1 > -1) {
      content[i-1].insert += content[i].insert;
      content.splice(i, 1);
      continue;
    }
    i++;
  }
  // 过滤所有的值的头部 \n
  for (let i = 0;i < content.length;) {
    let item = content[i];
    let insert = item.insert;
    if(insert.startsWith('\n')) {
        item.insert = insert.substring(insert.indexOf('\n')+1);
        continue;
    }
    if(insert.length == 0){
        content.splice(i, 1);
        continue;
    }
    i++
  }
  console.log('after formatting', content);
  return JSON.stringify(content);
}

